import type { TieredLabExam } from './types';

function tier(
	tierId: string,
	slugSuffix: string,
	levelEs: string,
	levelEn: string,
	titleEs: string,
	titleEn: string,
	descEs: string,
	descEn: string,
	authMode: 'none' | 'login' | 'subscription',
	questions: TieredLabExam['tiers'][0]['questions'],
) {
	return {
		tierId,
		slug: slugSuffix,
		titleEs,
		titleEn,
		levelEs,
		levelEn,
		descriptionEs: descEs,
		descriptionEn: descEn,
		authMode,
		questions,
	};
}

